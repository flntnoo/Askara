export const getAllDecks = (): Deck[] => {
  return [...DECKS].sort((a, b) => a.sortOrder - b.sortOrder);
};

export const getDeckById = (id: string): Deck | undefined => {
  return DECKS.find((deck) => deck.id === id);
};

export const getDeckBySlug = (slug: string): Deck | undefined => {
  return DECKS.find((deck) => deck.slug === slug);
};

export const getDecksByCategory = (category: DeckCategory): Deck[] => {
  return getAllDecks().filter((deck) => deck.category === category);
};

export const getDeckCategoryLabel = (category: DeckCategory): string => {
  return DECK_CATEGORY_LABELS[category];
};
